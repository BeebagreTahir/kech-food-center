// TO highlite the section pizza burgers and other ------------------------------------------------------>
// Select all section headings with IDs
const sections = document.querySelectorAll("h3[id]"); // All section headings
const navLinks = document.querySelectorAll(".nav2 .items a");

window.addEventListener("scroll", () => {
    let currentSection = "";

    sections.forEach(section => {
        const sectionTop = section.offsetTop - 120; // offset for fixed top nav
        const sectionHeight = section.offsetHeight;

        if (window.scrollY >= sectionTop && window.scrollY < sectionTop + sectionHeight) {
            currentSection = section.getAttribute("id");
        }
    });

    navLinks.forEach(link => {
        link.parentElement.classList.remove("active");
        if (link.getAttribute("href") === `#${currentSection}`) {
            link.parentElement.classList.add("active");

            // Scroll the active link into center for horizontal nav (mobile)
            link.scrollIntoView({ behavior: "smooth", inline: "center" });
        }
    });
});

